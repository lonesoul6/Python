# Calculate the slope, x-intercept and y-intercept of y = 2x -2

# slope of intercepts formula y=mx+b

# Equation: y = 2x - 2

# Define the coefficients
m = 2  # Slope
b = -2  # y-intercept

# Calculate x-intercept
x_intercept = -b / m

# Print results
print("Slope (m):", m)
print("y-intercept (b):", b)
print("x-intercept:", x_intercept)


